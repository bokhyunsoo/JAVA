package ch02;

public class Ex05 {
	public static void main(String[] args) {
		boolean b=false; //논리형 변수 : true, false만 저장됨
		System.out.println(b);
		System.out.println(!b); // 논리부정, not b
		System.out.println(10 > 5);
		System.out.println(10 < 5);
		System.out.println(b == false); //equal(같다)
		System.out.println(b != true); //not equal(같지않다)
	}
}
